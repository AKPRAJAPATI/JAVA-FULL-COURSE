package Mathe;

public class parse_int {
    public static void main(String[] args) {
        String x = "10";

        System.out.println(x+10);
        System.out.println(Integer.parseInt(x+10));
        System.out.println(Float.parseFloat(x+10));
        System.out.println(Double.parseDouble(x+10));
    }

}
